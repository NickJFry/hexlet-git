def collect_indexes(items):
    result = defaultdict(list) # В переменной создаётся словарь, где значения по умолчанию будут списковыми
    for index, item in enumerate(items): # Распаковываем список из items на индексы и элементы (цифры и буквы)
        result[item].append(index) # В созданный словарь к каждому ключу добавляется значение
    return result
