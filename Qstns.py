def collect_indexes(item): # Предоплагаемый аргумент - список или строка
    dt = {} # Создаём пустой словарь
    ls = list(item) # Преобразуем аргумент в спиоск, чтобы потом его распаковать
    for elem, i in enumerate(ls): # Распаковываем преобразованный список из item на индексы и элементы (цифры и буквы)
        if i in dt: # Если элемент (буква) уже существует в словаре по обходу цикла, то:
            dt[i].append(elem) # К этому элементу (букве), как к ключу словаря, добавить в списковое значение новый элемент к уже существующим
        else:
            dt.setdefault(i, [elem]) # Если элемент (буква) в словаре уникальный, то значением по ключу будет просто его номер в списке
    return dt

